		<div class="space-y-5 text-base leading-[1.65] text-[#3d342a] max-w-[62ch]">
			<p class="m-0">
				<span class="font-fraunces font-black text-[#1c1814]">HistoryonStamps</span> is the creation
				of researchers working on <em class="font-fraunces">Philately in Troubled Times</em>, a
				project which explores how history, memory, and identity are conveyed through stamps.
			</p>

			<p class="m-0">
				Inspired by a table created by historian Eric Hobsbawm in 1983 showing the interval between
				the first stamp issued by thirteen European countries and their first stamp commemorating
				an historical event, our team researched and created a new database showing the same
				interval for all existing and formerly existing countries across the globe.
			</p>

			<p class="m-0">
				We also created new categories of analysis, including identifying each stamp by location
				(country and regions), topic, and theme, Scott and Stanley Gibbons catalogue numbers, with
				images and links to the user-generated platform StampWorld.
			</p>

			<p class="m-0">
				Our team hopes that by offering an accessible database of the first historical stamp for
				over two-hundred countries will prove a useful way for historians/public historians and
				philatelists/stamp collectors to share enthusiasms and knowledge.
			</p>
		</div>

